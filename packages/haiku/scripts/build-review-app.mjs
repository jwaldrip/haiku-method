#!/usr/bin/env node
/**
 * Build the review SPA and embed the output as a TypeScript string constant.
 *
 * 1. cd review-app && npm run build
 * 2. Read dist/index.html and inline all CSS/JS assets into it
 * 3. Write to src/review-app-html.ts as: export const REVIEW_APP_HTML = `...`
 */
import { execSync } from "node:child_process";
import { readFileSync, writeFileSync, existsSync, readdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dir = dirname(fileURLToPath(import.meta.url));
const root = join(__dir, "..");
const reviewAppDir = join(root, "review-app");
const distDir = join(reviewAppDir, "dist");
const distHtml = join(distDir, "index.html");
const tsFile = join(root, "src", "review-app-html.ts");

// Install deps if needed
if (!existsSync(join(reviewAppDir, "node_modules"))) {
  console.error("Installing review-app dependencies...");
  execSync("npm install", { cwd: reviewAppDir, stdio: "inherit" });
}

// Build the review app
console.error("Building review SPA...");
execSync("npm run build", { cwd: reviewAppDir, stdio: "inherit" });

// Read the built HTML
if (!existsSync(distHtml)) {
  console.error(`ERROR: Expected ${distHtml} to exist after build`);
  process.exit(1);
}

let html = readFileSync(distHtml, "utf-8");

// Inline all <script> and <link rel="stylesheet"> assets
// Replace <script type="module" crossorigin src="/assets/xxx.js"></script>
html = html.replace(
  /<script\b[^>]*\bsrc="\/assets\/([^"]+)"[^>]*><\/script>/g,
  (match, filename) => {
    const filePath = join(distDir, "assets", filename);
    if (!existsSync(filePath)) {
      console.error(`WARNING: Referenced asset not found: ${filePath}`);
      return match;
    }
    const content = readFileSync(filePath, "utf-8");
    return `<script type="module">${content}</script>`;
  },
);

// Replace <link rel="stylesheet" crossorigin href="/assets/xxx.css">
html = html.replace(
  /<link\b[^>]*\bhref="\/assets\/([^"]+\.css)"[^>]*>/g,
  (match, filename) => {
    const filePath = join(distDir, "assets", filename);
    if (!existsSync(filePath)) {
      console.error(`WARNING: Referenced CSS asset not found: ${filePath}`);
      return match;
    }
    const content = readFileSync(filePath, "utf-8");
    return `<style>${content}</style>`;
  },
);

// Write it as a TypeScript string export
const tsContent = `// AUTO-GENERATED by scripts/build-review-app.mjs \u2014 do not edit manually.\nexport const REVIEW_APP_HTML: string = ${JSON.stringify(html)};\n`;
writeFileSync(tsFile, tsContent);

const bytes = Buffer.byteLength(html, "utf-8");
console.error(`Review SPA (inlined): ${bytes} bytes -> ${tsFile}`);
